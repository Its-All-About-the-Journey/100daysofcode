# Indentation
# Function is like a folder

# All code belongs to function
def my_function():
    print("Hello")
    print("World")

# Non-indented line does not belong to function
def my_function1():
    print("Hello")
print("World")

# Fnction block, IF block & ELIF block
sky = "clear"
def my_function2():
    if sky == "clear":
        print("blue")
    elif sky == "cloudy":
        print("grey")
    print("Hello")
print("World")
