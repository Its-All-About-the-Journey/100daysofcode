# Format the display

def print_display(display):
  return """| | {:>15} | |  .----------------.  .----------------.  .----------------.  .----------------.""".format(display)

# Stitch formatted display and parts together and print the logo.

def print_logo(value):

  if len(str(value)) > 15:
#    value = str(value)[:15]
    value = "OVERFLOW"

  logo = calculator_top
  if not value:
    logo += print_display("Pythonista   0.")
  else:
    logo += print_display(value)
  print(logo + calculator_bottom)

