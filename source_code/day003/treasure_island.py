crossroad = input(
    "You're at a crossroad. Do you want to go right or left?\n").lower()
if crossroad == 'left':
    swim = input(
        "You need to get to the island. Would you like to swim or wait for a boat?\n").lower()
    if swim == 'wait':
        door = input(
            "You are presented with three doors: red, blue, yellow. Which color door would you like to go through?\n").lower()
        if door == 'red':
            print("You were burned by fire. Game over.\n")
        elif door == 'blue':
            print("You were eaten by beasts. Game over.\n")
        elif door == 'yellow':
            print("You win!")
        else:
            print("Game over.")
    else:
        print("You were attacked by a trout. Game over.\n")
else:
    print("You fell into a hole. Game over.\n")
